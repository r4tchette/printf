int	is_num(char c)
{
	if ('0' <= c && c <= '9')
		return (1);
	return (0);
}

int	is_flag(char c)
{
	if (c == '-' || c == '+' || c == ' ' ||\
		c == '#' || c == '.' || c == 'h' ||\
		c == 'l' || c == '0' || c == 'H' ||\
		c == 'L')
		return (1);
	return (0);
}

int	is_type(char c)
{
	if (c == 'c' || c == 's' || c == 'p' ||\
		c == 'd' || c == 'i' || c == 'u' ||\
		c == 'x' || c == 'X' || c == '%' ||\
		c == 'n' || c == 'f')
		return (1);
	return (0);
}